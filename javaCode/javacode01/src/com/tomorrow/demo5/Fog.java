package com.tomorrow.demo5;

public class Fog extends Animal implements swim {


    public Fog() {
    }

    public Fog(String name, int age) {
        super(name, age);
    }

    @Override
    public void eat() {
        System.out.println("青蛙在吃虫子");
    }

    @Override
    public void swim() {
        System.out.println("青蛙在蛙泳");
    }
}
